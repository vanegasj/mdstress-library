    static inline bool iszeromatrix ( dmatrix A )
    {   
        for ( int i = 0; i < mds_ndim; i++ )
            for ( int j = 0; j < mds_ndim; j++ )
                if (A[i][j] > mds_eps) return false;
                
        return true;
    }

