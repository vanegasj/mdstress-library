    static inline void sumarray ( darray a, darray b, darray c)
    {
        c[0] = a[0]+b[0];
        c[1] = a[1]+b[1];
        c[2] = a[2]+b[2];
    }

    static inline void sumarray6 ( darray6 a, darray6 b, darray6 c)
    {
        c[0] = a[0]+b[0];
        c[1] = a[1]+b[1];
        c[2] = a[2]+b[2];
        c[3] = a[3]+b[3];
        c[4] = a[4]+b[4];
        c[5] = a[5]+b[5];
    }

    static inline void diffarray ( darray a, darray b, darray c)
    {
        c[0] = a[0]-b[0];
        c[1] = a[1]-b[1];
        c[2] = a[2]-b[2];
    }

    static inline void diffarray ( darray a, darray b, darray c, dmatrix box, barray periodic)
    {
        c[0] = a[0]-b[0];
        c[1] = a[1]-b[1];
        c[2] = a[2]-b[2];

        if (periodic[3] == true)
        {
            for (int i = 0; i < mds_ndim; i++)
            {
                if (periodic[i] == true)
                {
                    while (c[i] > 0.5*box[i][i])
                        c[i] -= box[i][i];
                    while (c[i] <= -0.5*box[i][i])
                        c[i] += box[i][i];
                }
            }
        }
    }

    static inline void scalearray ( darray b, double a, darray c)
    {
        c[0] = a * b[0];
        c[1] = a * b[1];
        c[2] = a * b[2];
    }

    static inline double normarray ( darray a )
    {
        return sqrt(a[0]*a[0] + a[1]*a[1] + a[2]*a[2]);
    }

    static inline void copyarray ( darray a, darray b)
    {
        b[0] = a[0];
        b[1] = a[1];
        b[2] = a[2];
    }

    static inline void summatrix ( dmatrix a, dmatrix b, dmatrix c)
    {
        c[0][0] = a[0][0] + b[0][0];
        c[0][1] = a[0][1] + b[0][1];
        c[0][2] = a[0][2] + b[0][2];
        c[1][0] = a[1][0] + b[1][0];
        c[1][1] = a[1][1] + b[1][1];
        c[1][2] = a[1][2] + b[1][2];
        c[2][0] = a[2][0] + b[2][0];
        c[2][1] = a[2][1] + b[2][1];
        c[2][2] = a[2][2] + b[2][2];
    }

